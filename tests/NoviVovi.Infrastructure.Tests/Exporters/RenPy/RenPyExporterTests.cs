    public RenPyExporterTests()
    {
        _mockRepository = new Mock<INovelRepository>();
        _mockStorage = new Mock<IStorageService>();
        _mockResourceLoader = new Mock<IEmbeddedResourceLoader>();

        // Setup real services (no mocks for business logic)
        var identifierGenerator = new RenPyIdentifierGenerator();
        var transformMapper = new TransformToRenPyMapper();
        var characterMapper = new CharacterToRenPyMapper(identifierGenerator);
        var stepMapper = new StepToRenPyMapper(identifierGenerator, transformMapper);
        var labelMapper = new LabelToRenPyMapper(identifierGenerator, stepMapper);
        var novelMapper = new NovelToRenPyMapper(identifierGenerator, characterMapper, labelMapper);
        
        var statementRenderer = new RenPyStatementRenderer();
        var scriptGenerator = new RenPyScriptGenerator(_mockResourceLoader.Object, statementRenderer);
        
        var imageCollector = new NovelImageCollector();
        var imageExporter = new RenPyImageExporter(_mockStorage.Object);
        
        var archiveBuilder = new RenPyArchiveBuilder(_mockResourceLoader.Object);

        _exporter = new RenPyExporter(
            _mockRepository.Object,
            novelMapper,
            scriptGenerator,
            imageCollector,
            imageExporter,
            archiveBuilder
        );

        // Setup template mock
        var templateContent = @"## Generated by NoviVovi - {{ title }}

## Character Definitions
{{~ for character in characters ~}}
define {{ character.variable_name }} = Character(""{{ character.display_name }}"", color=""{{ character.color }}"")
{{~ end ~}}

## Game Start
label start:
{{~ for label in labels ~}}
{{~ if label.identifier == start_label_id ~}}
{{~ for statement in label.statements ~}}
{{ render_statement statement }}
{{~ end ~}}
{{~ end ~}}
{{~ end ~}}
    return

## Labels
{{~ for label in labels ~}}
{{~ if label.identifier != start_label_id ~}}

label {{ label.identifier }}:
{{~ for statement in label.statements ~}}
{{ render_statement statement }}
{{~ end ~}}
    return
{{~ end ~}}
{{~ end ~}}";

        _mockResourceLoader
            .Setup(x => x.LoadTextResourceAsync(
                "NoviVovi.Infrastructure.Exporters.RenPy.Templates.script.rpy.sbn",
                It.IsAny<CancellationToken>()))
            .ReturnsAsync(templateContent);

        _mockResourceLoader
            .Setup(x => x.LoadStreamResourceAsync(
                "NoviVovi.Infrastructure.Exporters.RenPy.Resources.BaseProject.zip",
                It.IsAny<CancellationToken>()))
            .ReturnsAsync((Stream?)null);
    }

    [Fact]
    public async Task ExportToRenPyAsync_MinimalNovel_ReturnsValidZip()
    {
        // Arrange
        var novel = Novel.Create("Test Novel", "start");
        var character = Character.Create("Narrator", Color.FromHex("#ffffff"), null);
        novel.AddCharacter(character);
        
        var replica = Replica.Create(character, "Hello World");
        var replicaStep = ShowReplicaStep.Create(replica);
        novel.StartLabel.AddStep(replicaStep);

        _mockRepository
            .Setup(x => x.GetByIdAsync(novel.Id, It.IsAny<CancellationToken>()))
            .ReturnsAsync(novel);

        // Act
        var zipBytes = await _exporter.ExportToRenPyAsync(novel.Id, CancellationToken.None);

        // Assert
        Assert.NotNull(zipBytes);
        Assert.NotEmpty(zipBytes);

        // Verify ZIP structure
        using var memoryStream = new MemoryStream(zipBytes);
        using var archive = new ZipArchive(memoryStream, ZipArchiveMode.Read);

        var scriptEntry = archive.GetEntry("game/script.rpy");
        Assert.NotNull(scriptEntry);

        using var scriptStream = scriptEntry.Open();
        using var reader = new StreamReader(scriptStream, Encoding.UTF8);
        var scriptContent = await reader.ReadToEndAsync();

        Assert.Contains("Test Novel", scriptContent);
        Assert.Contains("define char_", scriptContent);
        Assert.Contains("Hello World", scriptContent);
    }

