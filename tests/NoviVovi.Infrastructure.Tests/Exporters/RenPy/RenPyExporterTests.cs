    [Fact]
    public async Task ExportToRenPyAsync_ZipContainsGameDirectory()
    {
        // Arrange
        var novel = Novel.Create("Test", "start");
        var character = Character.Create("Test", Color.FromHex("#ffffff"), null);
        novel.AddCharacter(character);

        _mockRepository
            .Setup(x => x.GetByIdAsync(novel.Id, It.IsAny<CancellationToken>()))
            .ReturnsAsync(novel);

        // Act
        var zipBytes = await _exporter.ExportToRenPyAsync(novel.Id, CancellationToken.None);

        // Assert
        using var memoryStream = new MemoryStream(zipBytes);
        using var archive = new ZipArchive(memoryStream, ZipArchiveMode.Read);

        var gameEntries = archive.Entries.Where(e => e.FullName.StartsWith("game/")).ToList();
        Assert.NotEmpty(gameEntries);
        Assert.Contains(gameEntries, e => e.FullName == "game/script.rpy");
    }

